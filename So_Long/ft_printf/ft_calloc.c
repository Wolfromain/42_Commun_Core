#include "ft_printf.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	char	*dest;

	if ((nmemb * size < nmemb && size != 0)
		|| (nmemb * size < size && nmemb != 0))
		return (NULL);
	dest = malloc(nmemb * size);
	if (!dest)
		return (NULL);
	ft_bzero(dest, nmemb * size);
	return (dest);
}
